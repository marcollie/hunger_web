$('#btnsoupe').click(function(e){    
  $('#menutable .tblshow').fadeOut('slow').promise().done(function(){
      $('#tblsoupe').fadeIn('slow');
  });
  $('.menutitle ul li').css('background-color', 'white');
  $('#btnsoupe').css('background-color', '#E8C300');
});

